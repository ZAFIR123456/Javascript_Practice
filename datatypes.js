// Data types in computer programming are used to categorize and represent different types of data
// that can be used in a program. Each data type defines the size, format, and operations that can be
//  performed on the data. In JavaScript, there are several built-in data types:


// ******************************
// 1. PRIMITIVE DATA TYPES
// ******************************



// * Number
//  Represents both integer and floating-point numbers. For example, 42 or 3.14.
    
    let number = 42;

// * String
// Represents a sequence of characters. Enclosed in single ('') or double quotes (""). 
// For example, 'Hello' or "JavaScript".

    let String = "Hello world"

// * Boolean
// Represents a logical value true or false.

    let Boolean = true

// * Null
//  Represents an intentional absence of any value.

    let empty = null

// * Undefined
// Represents a declared variable that hasn't been assigned a value.

    let notDefined;  


// ******************************
// 2. NON-PRIMITIVE DATA TYPES
// ******************************



// * Array
// Represents a collection of elements in a specific order. For example, [1, 2, 3, 4].

    let num = [1, 2, 3, 4, 5, 6]

// * Objects
// Represents a collection of key-value pairs. For example, { name: 'John', age: 25 }.

    let person = {
        name:"zafir",
        age:20
    }

// * Functons
// Represents a reusable block of code that performs a specific task.

    let hello = function(){
        console.log("hello")
    }
